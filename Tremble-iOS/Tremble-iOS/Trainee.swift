import Foundation

class Trainee {
    
    var firstname:String
    var lastname:String
    var grade:String
    var subject:String
    
    init (firstname:String, lastname:String, grade:String, subject:String) {
      
        self.firstname = firstname
        self.lastname = lastname
        self.grade = grade
        self.subject = subject
    }
    
}